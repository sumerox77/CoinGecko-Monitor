        JButton jButton = new JButton();
        jButton.setText("DEL");
        jButton.setMaximumSize(new Dimension(100, 40));
        jButton.addActionListener(ae -> {
            triggerManager.remove(te.getId());
            redrawTriggers();
        });

        jPanel.add(jButton);
